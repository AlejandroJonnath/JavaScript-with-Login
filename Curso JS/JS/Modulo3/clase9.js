//OPERADORES
//Hay operadores aritméticos, asignación, comparación,lógicos, Bitwise

//Operadores Aritméticos

let a = 5;//recuerda que ambas son declaraciones
let b = 7;

//Suma
console.log(a+b, "Adición")// recuerda que esto es una expresión

//Resta
console.log(a-b, "Resta")// recuerda que esto es una expresión

//Multiplicación
console.log(a*b, "Multiplicación")// recuerda que esto es una expresión

//División
console.log(a/b, "División")// recuerda que esto es una expresión

//Módulo
//El módulo es lo que sobra en una división, 7/5= 2, el dos es el módulo
// 5/7 = 5
console.log(a%b, "Módulo")// recuerda que esto es una expresión

//Potencia
//(a elevado a la b)
console.log(a**b, "Potencia")// recuerda que esto es una expresión 

//Operadores Incremento, Decrement

//incrementar
console.log(++a) //incrementa el valor de a en 1
console.log(a++)//imprime el valor de a y luego le suma 1
console.log(a)
//decrement
console.log(--a) //decrement el valor de a en 1
console.log(a--)//imprime el valor de a y luego le resta 1
console.log(a)

